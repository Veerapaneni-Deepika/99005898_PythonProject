def add(num1, num2):
    'Function to add'
    return num1+num2

def multiply(num1, num2):
    'Function to multiply'
    return num1*num2

def even(num):
    'Function to check if even'
    return num%2==0

def odd(num):
    'Function to check if odd'
    return num%2!=0
